//w = width, h = height, m = margin, c = coordinate
namespace StickyScreen{
  public class Kata{
    public static double[] newCoordinate(int w, int h, double m, double[] c) =>
      c[0] > w || c[1] > h || c[0] < 0 || c[1] < 0 ?                   //check out of bound, return 0,0 if true
      new double[]{0,0} :
      new double[]{(c[0] <= m ? 0 : c[0] >= w - m ? w : c[0]),         //sticky margins for width and height
                   (c[1] <= m ? 0 : c[1] >= h - m ? h : c[1])};
  }
}

