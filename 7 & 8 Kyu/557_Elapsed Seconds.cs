//find the difference in date, convert to total seconds and return result.
using System;


public class Kata{
  public static int ElapsedSeconds(DateTime startDate, DateTime endDate) => (int)(endDate - startDate).TotalSeconds;
}

//****************Sample Test*****************
namespace Solution {
  using NUnit.Framework;
  using System;

  [TestFixture]
  public class KataTests
  {
    [Test]
    public void BasicTests()
    {
      var start = new DateTime(2013, 1, 1, 0, 0, 1);
      var end = new DateTime(2013, 1, 1, 0, 0, 2);
      var end2 = new DateTime(2013, 1, 1, 0, 0, 20);
      var end3 = new DateTime(2013, 1, 1, 0, 1, 20);
      Assert.AreEqual(1, Kata.ElapsedSeconds(start, end));
      Assert.AreEqual(19, Kata.ElapsedSeconds(start, end2));
      Assert.AreEqual(79, Kata.ElapsedSeconds(start, end3));
    }
    
    [Test]
    public void ExtendedTests()
    {
      var start = new DateTime(2013, 1, 1, 0, 0, 1);
      
      Assert.AreEqual(302, Kata.ElapsedSeconds(start, start.AddSeconds(302)));
      Assert.AreEqual(3600, Kata.ElapsedSeconds(start, start.AddSeconds(3600)));
      Assert.AreEqual(-200, Kata.ElapsedSeconds(start, start.AddSeconds(-200)));
    }
    
    [Test]
    public void RandomTests()
    {
      var rand = new Random();
      var start = new DateTime(2013, 1, 1, 0, 0, 1);
      for(int r=0;r<100;r++)
      {
        var elapsed = rand.Next(-2000,2001);
        Assert.AreEqual(elapsed, Kata.ElapsedSeconds(start, start.AddSeconds(elapsed)));
      }
    }
  }
}
