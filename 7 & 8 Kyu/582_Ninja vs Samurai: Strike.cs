using System;

public class Warrior{
    private string Name;
    public int Health { get; set; } = 100;
  
    public Warrior(string name) => Name = name;
  //removed static
    public void Strike(Warrior enemy, int swings) => enemy.Health = Math.Max(0, enemy.Health - (swings * 10));
}

//****************Sample Test*****************
using System;
using NUnit.Framework;

[TestFixture]
public class KataTest
{
    private Warrior ninja = new Warrior("Ninja");
    private Warrior samurai = new Warrior("Samurai");

    [Test]
    public void Tests()
    {
        samurai.Strike(ninja, 4);
        Assert.AreEqual(60, ninja.Health);
    }
}
