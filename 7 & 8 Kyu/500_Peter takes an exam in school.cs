using System.Collections.Generic;
using System.Linq;

public class Kata{
        public static List<string> PassedStudentList(List<Student> gradeList, int acceptanceGrade) =>
          gradeList.Where(x => x.Grade >= acceptanceGrade)
                   .Select(y => y.Name)
                   .OrderBy(z => z)
                   .ToList();
}

