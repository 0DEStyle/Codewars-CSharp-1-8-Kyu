using NUnit.Framework;
using System;
using System.Linq;
[TestFixture]
public class HistogramTest
{
    [Test]
    public void SimpleTest()
    {
        uint[] data = new uint[] {1, 1, 0, 1, 3, 2, 6};
        Assert.AreEqual(new uint[] {1, 3, 1, 1, 0, 0, 1}, Kata.Histogram(data, 1));
        Assert.AreEqual(new uint[] {4, 2, 0, 1}, Kata.Histogram(data, 2));
    }
    [Test]
    public void EmptyTest()
    {
        uint[] data = new uint[0];
        Assert.AreEqual(new uint[0], Kata.Histogram(data, 1));
    }
    [Test]
    public void ZerosTest()
    {
        uint[] data = new uint[]{8};
        Assert.AreEqual(new uint[]{0, 0, 0, 0, 0, 0, 0, 0, 1}, Kata.Histogram(data, 1));
    }
    [Test]
    public void RandomTest()
    {
        Random rnd = new Random();
        for (int i = 1; i < 100 ; i++) {
          uint n = (uint) rnd.Next(0, 300);
          uint binWidth = (uint) rnd.Next(1,31);
          uint[] data = new uint[]{n};
          uint[] result = new uint[n / binWidth + 1];
          result[n / binWidth] = 1;
          Assert.AreEqual(result, Kata.Histogram(data, binWidth));
      }
   }
