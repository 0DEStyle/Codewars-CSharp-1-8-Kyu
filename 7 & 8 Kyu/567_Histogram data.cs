//if data length is 0, return 0
//else create new array with length data.Max() / binWidth + 1
//x is current element, i is index
//select the element count where x /binWidth == i, parse to unsigned int
//store in array and return the result.
using System.Linq;

public static class Kata{
  public static uint[] Histogram(uint[] data, uint binWidth) =>
    data.Length == 0 ? data :
    new int[data.Max() / binWidth + 1].Select((x, i) => (uint)data.Count(x => x / binWidth == i))
                                      .ToArray();
}
