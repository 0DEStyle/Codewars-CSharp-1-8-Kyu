namespace Solution {
  using NUnit.Framework;
  using System;
  using System.Text;

  [TestFixture]
  public class SolutionTest
  {
  
    private static Random rnd = new Random();
    
    private static string solution(string sentence)
    {
      StringBuilder sb = new StringBuilder(sentence.Length);
      
      for (int i = 0; i < sentence.Length; ++i)
      {
        // Even idx
        if (i % 2 == 0) { sb.Append(Char.ToUpper(sentence[i])); }
        // Odd idx
        else { sb.Append(Char.ToLower(sentence[i])); }
      }
      
      return sb.ToString();
    }
    
    private static string newStr()
    {
      StringBuilder newString = new StringBuilder();
      int newWords = rnd.Next(1, 11);
      for (int i = 0; i < 500; ++i)
      {
        int numChar = rnd.Next(1, 7);
        StringBuilder newWord = new StringBuilder();
        for (int j = 0; j < numChar; ++j)
        {
          newWord.Append((char)rnd.Next(97, 123));
        }
        newString.Append(newWord.ToString());
        newString.Append(' ');
      }
      return newString.ToString().Trim();
    }
    
    private static object[] Basic_Test_Cases = new object[]
    {
      new object[] {"stop Making spongebob Memes!", "StOp mAkInG SpOnGeBoB MeMeS!"},
    };
  
    [Test, TestCaseSource(typeof(SolutionTest), "Basic_Test_Cases")]
    public void Basic_Test(string test, string expected)
    {
      Assert.AreEqual(expected, Kata.SpongeMeme(test));
    }
    
    [Test, Description("Random Tests (100 assertions)")]
    public void Random_Test()
    {
      for (int i = 0; i < 100; ++i)
      {
        string test = newStr();
        string expected = solution(test);
        string actual = Kata.SpongeMeme(test);
        Assert.AreEqual(expected, actual);
      }
    }
  }
