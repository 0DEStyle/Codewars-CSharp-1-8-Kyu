//x is current element, i is index
//from sentence, if index is even, convert to upper, else convert to lower
//concat the elements and return the result.
using System;
using System.Linq;
public class Kata{
  public static string SpongeMeme(string sentence) => 
    string.Concat(sentence.Select((x,i) => i % 2 == 0 ? Char.ToUpper(x) : Char.ToLower(x)));
}

