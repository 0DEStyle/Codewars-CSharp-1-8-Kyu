public class Quark {
  public string Color; //"red", "blue", and "green"
  public string Flavor; //'up', 'down', 'strange', 'charm', 'top', and 'bottom'
  public Quark(string color, string flavor){ Color = color; Flavor = flavor; }
  
  public double BaryonNumber => 1 / 3.0;  // 1/3 fraction
  public void Interact(Quark quark1) => (Color, quark1.Color) =  (quark1.Color, Color); //When two quarks interact they exchange colors.
}

