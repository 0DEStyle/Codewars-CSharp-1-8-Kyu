//if candies length is 0 or 1, return -1
//else get highest candies value and times the length of candies(max total), subtract by the current total
using System.Linq;

public class CandyProblem{
	public static int GetMissingCandies(int[] candies) => 
    candies.Length == 0 || candies.Length == 1 ? -1 : candies.Max() * candies.Length - candies.Sum();
}

