using System;
using System.Linq;

public class EvenLadder
{
  public static string Pattern(int n)
  {
    return string.Join("\n", Enumerable.Range(1, Math.Max(0, n)).Where(x => x % 2 == 0)
        .Select(x => string.Concat(Enumerable.Repeat(x.ToString(), x))));
  }
}

