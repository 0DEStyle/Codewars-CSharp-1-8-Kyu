public class EvenLadder{
  public static string Pattern(int n){
    string res = "";
    //start at 2, icnrease by 2 each time
    for(int i = 2; i <= n; i+=2){
      //repeat number i, j times.
      for(int j = 0; j < i; j++) 
        res += i;
      //append "\n" to res
      res += "\n";
    }
    //trim last "\n" and return the result.
    return res.TrimEnd('\n');
  }
}
