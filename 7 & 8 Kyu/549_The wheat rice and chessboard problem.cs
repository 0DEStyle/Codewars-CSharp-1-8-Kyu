using NUnit.Framework;
using System;
class SolutionTest
{
    [Test]
    public void FixedTest()
    {
        Assert.AreEqual(0, Kata.SquaresNeeded(0));
        Assert.AreEqual(1, Kata.SquaresNeeded(1));
        Assert.AreEqual(2, Kata.SquaresNeeded(2));
        Assert.AreEqual(2, Kata.SquaresNeeded(3));
        Assert.AreEqual(3, Kata.SquaresNeeded(4));
    }

    [Test]
    public void RandomTest()
    {
        var random = new Random();
        for (int i = 0; i < 100; i++)
        {
            var r = (long)(random.NextDouble() * Math.Pow(10, random.Next(1, 15))) + 1;
            Console.WriteLine(r+" "+SquaresNeeded(r));
            Assert.AreEqual(SquaresNeeded(r), Kata.SquaresNeeded(r));
        }
    }

    private static int SquaresNeeded(long grains) => (int)Math.Ceiling(Math.Log(grains + 1, 2));
