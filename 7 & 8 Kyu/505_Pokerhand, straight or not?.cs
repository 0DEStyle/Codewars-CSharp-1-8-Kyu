namespace Poker {
  using NUnit.Framework;
  using System;
  using System.Collections.Generic;
  using System.Linq;
  
  [TestFixture]
  public class PokerHandTest
  {
    private static Random rng = new Random(DateTime.Now.Ticks.GetHashCode());
    
    [TestCase(new[] { 2, 3, 4 ,5, 6}, true, "2, 3, 4 ,5, 6")]
    [TestCase(new[] { 14, 5, 4 ,2, 3}, true, "14, 5, 4 ,2, 3")]
    [TestCase(new[] { 2, 3}, false, "2, 3")]
    [TestCase(new[] { 7, 7, 12 ,11, 3, 4, 14}, false, "7, 7, 12 ,11, 3, 4, 14")]
    [TestCase(new[] { 2, 7, 12 ,11, 3, 4, 14}, false, "2, 7, 12 ,11, 3, 4, 14")]
    [TestCase(new[] { 10, 7, 12 ,11, 13, 4, 14}, true, "10, 7, 12 ,11, 13, 4, 14")]
    [TestCase(new[] { 10, 14, 14 ,13, 12, 10, 11}, true, "10, 14, 14 ,13, 12, 10, 11")]
    [TestCase(new[] { 2, 7, 12 ,11, 10, 13, 14}, true, "2, 7, 12 ,11, 10, 13, 14")]
    [TestCase(new[] { 3, 4, 5 ,6, 8, 9, 10}, false, "")]
    public void IsStraight(IEnumerable<int> cardValues, bool isStraight, string errorMessage)
    {
      var cards = new List<Card>();
      foreach(var cardValue in cardValues) {
        cards.Add(new Card { CardValue = cardValue });
      }
      
      Assert.AreEqual(isStraight, PokerHand.IsStraight(cards), errorMessage); 
    }
    
    [Test]
    public void IsStraight_AllVariations() {
      var cardValues = new[] { 14, 2, 3, 4, 5, 6, 7, 8, 9, 10 };
      foreach(var value in cardValues) {
        var range = Enumerable.Range(value == 14 ? 2 : value +1, 4).ToList();
        range.Add(value);
        range.Add(rng.Next(2, 14));
        range.Add(rng.Next(2, 14));
        range = Shuffle(range);
        var cards = range.Select(r => new Card { CardValue = r }).ToList();
        
        Assert.IsTrue(PokerHand.IsStraight(cards), string.Join(", ", range.Select(r => r)));
      }
    }
      
      private List<int> Shuffle(List<int> list)  
      {  
        var n = list.Count;  
        while (n > 1) {  
          n--;  
          var k = rng.Next(n + 1);  
          var value = list[k];  
          list[k] = list[n];  
          list[n] = value;  
        }  
        return list;
      }
    }
