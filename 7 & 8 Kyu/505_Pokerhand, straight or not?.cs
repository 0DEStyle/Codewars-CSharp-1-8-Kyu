using System.Collections.Generic;
using System.Linq;
  
namespace Poker {
    public static class PokerHand{
        public static bool IsStraight(List<Card> cards){
          //if 14 exist, add 1 to the cards List.
          if (cards.Any(x => x.CardValue == 14))
            cards.Add(new Card{CardValue = 1});
          //generate sequence, start from 1, count up to 14
          //x and y are current elements
          //if the current x(index) matches any card value y, return T(true), else F(False), convert the result to array
          //check if all 5 hands are true and return a bool value.
          return new string(Enumerable.Range(1, 14).Select(x => cards.Any(y => y.CardValue == x) ? 'T' : 'F').ToArray())
                                              .Contains("TTTTT");
          }
    }
    public class Card {
        public int CardValue { get; set; }
    }
}

