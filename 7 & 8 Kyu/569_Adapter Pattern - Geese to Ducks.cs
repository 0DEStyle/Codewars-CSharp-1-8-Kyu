using System;

public class GooseToIDuckAdapter : IDuck{
  Goose honker;
  public GooseToIDuckAdapter(Goose goose) => honker = goose;
  public string Quack() => honker.Honk();
  public void Fly() => honker.Fly();
}

//****************Sample Test*****************
using System;
using NUnit.Framework;

[TestFixture]
public class KataTestf
{
    [Test]
    public void _0_AdapterTypeCheck()
    {
        Goose goose = new Goose();
        GooseToIDuckAdapter adapter = new GooseToIDuckAdapter(goose);

        Assert.IsInstanceOf<IDuck>(adapter, "Class GooseToIDuckAdapter must inherit IDuck interface.");
    }
    
    [Test]
    public void _1_AdapterInstanceCheck()
    {
        var instances = Goose.DucksCreated;
    
        Goose goose = new Goose();
        GooseToIDuckAdapter adapater = new GooseToIDuckAdapter(goose);
        
        Assert.AreEqual(instances + 1, Goose.DucksCreated, "No extra geese should be constructed!");
    }
    
    [Test]
    public void _2_AdapterMethodSupport()
    {
        Goose goose = new Goose();
        GooseToIDuckAdapter adapter = new GooseToIDuckAdapter(goose);
        
        var startHonks = goose.Honks;
        var startFlights = goose.Flights;
            
        goose.Honk();
        goose.Fly();
            
        adapter.Quack();
        adapter.Fly();
            
        Assert.AreEqual(startHonks + 2, goose.Honks, $"The original goose should be able to quack like a duck!\n" + 
                                                     $"Expected {startHonks + 2} quacks, but was {goose.Honks}");
            
        Assert.AreEqual(startFlights + 2, goose.Flights, $"The original goose should be able to fly like a duck!\n" + 
                                                         $"Expected {startFlights + 2} flights, but was {goose.Flights}");
    }
    
    [Test]
    public void _3_ExplicitQuackTest()
    {
        Goose goose = new Goose();
        GooseToIDuckAdapter adapter = new GooseToIDuckAdapter(goose);
        
        Assert.AreEqual(goose.Sound, adapter.Quack(), "Adapter should be returning the sound a Goose makes!");
        
        goose.Sound = "Fake Quack!";
        
        Assert.AreEqual(goose.Sound, adapter.Quack(), "Adapter should return the Goose's sound, not it's own!");
    }
    
    [Test]
    public void _4_StressTest()
    {
        Goose goose = new Goose();
        GooseToIDuckAdapter adapter = new GooseToIDuckAdapter(goose);
        
        goose.Fly();
        
        var startHonks = goose.Honks;
        var startFlights = goose.Flights;
        
        var largeNumber = (new Random()).Next(5000, 50000);
        
        for(var i = 0; i < largeNumber; ++i)
        {
            adapter.Quack();
            adapter.Fly();
        }
        
        Assert.AreEqual(startHonks + largeNumber, goose.Honks);
        Assert.AreEqual(startFlights + largeNumber, goose.Flights);
    }
}
