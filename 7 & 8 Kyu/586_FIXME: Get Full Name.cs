public class Dinglemouse{
  private string FirstName;
  private string LastName;
  public string FullName => $"{FirstName} {LastName}".Trim();
  
  public Dinglemouse(string firstName, string lastName){
    FirstName = firstName;
    LastName = lastName;
  }
}
