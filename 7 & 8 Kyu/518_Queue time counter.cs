                arr[j] = random.Next(1, (int)Math.Pow(10, random.Next(1, 4)));
            var pos = random.Next(random.Next(arr.Length));
            Assert.AreEqual(Queue(arr, pos), Kata.Queue(arr, pos));
        }
    }

    private static int Queue(int[] queuers, int pos) => queuers.Select((x, i) => Math.Min(x, queuers[pos] + (i <= pos ? 0 : -1))).Sum();
