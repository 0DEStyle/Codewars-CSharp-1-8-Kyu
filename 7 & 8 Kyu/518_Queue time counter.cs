//sum the result.

/*e.g.
{ 2, 5, 3, 6, 4 }, 3 
min values 
(2, 6 + 0) => 2
(5, 6 + 0) => 5
(3, 6 + 0) => 3
(6, 6 + 0) => 6
(4, 6 + -1) => 4
       sum = 20
*/
using static System.Math;
using System.Linq;
class Kata{
    public static int Queue(int[] q, int pos) => 
      q.Select((x, i) => Min(x, q[pos] + (i <= pos ? 0 : -1))).Sum();
}


//solution 2
  internal class Kata
{
    public static int Queue(int[] queuers, int pos)
    {
        var count = 0;
        var nextIdx = 0;
        while (queuers[pos] > 0)
        {
            if (queuers[nextIdx] > 0)
                count++;

            queuers[nextIdx] -= 1;
            nextIdx = (nextIdx + 1) % queuers.Length;
        }

        return count;
    }
}
