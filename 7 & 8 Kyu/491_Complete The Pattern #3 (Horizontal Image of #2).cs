using System.Linq;
using System;
public class HorizontalImageOfPart2
{
  public static string Pattern(int n)
  {
     return string.Join("\n",
                   Enumerable.Range(0,Math.Max(n,0)).Select(i=> 
                           string.Concat(Enumerable.Range(0,i+1).Select(j=> n-j)))
                       );
  }
}
