using System;

public class HorizontalImageOfPart2{
  public static string Pattern(int n){
    string res = "", next = "";
    //create sequence store in next, then add next + "\n" to res
    for(int i = n; i > 0; i--){
      next += i;
      res +=  next + "\n";
    }
    
    //print jank, trime the last newLine and return the result.
    //Console.WriteLine(res);
    return res.TrimEnd('\n');
  }
}

