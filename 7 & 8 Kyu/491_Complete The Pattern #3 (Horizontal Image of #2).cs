    [Test]
    public void RandomTests()    
    {
      var rand = new Random();
      
      Func<int,string> myPattern = delegate (int n)
      {
        if(n < 1)
        {
          return "";
        }
    
        var output="";
        for(var i = n; i > 0; i--)
        {
          output += string.Concat(Enumerable.Range(0, n - i + 1).Select((a,b) => (n - b)));
      
          if(i > 1)
          {
            output += '\n';
          }
        }
        return output;
      };
      
      for(int i = 0; i < 20; i++)
      {
        var n = rand.Next(-2, 100);
        
        Console.WriteLine("Random test for " + n);
        
        Assert.AreEqual(myPattern(n), HorizontalImageOfPart2.Pattern(n));
      }
    }
