    private int SaveSolution(int[] sizes, int hd)
    {
      int storedFiles = 0;
    
      for(int i = 0; i < sizes.Length; i++)
      {
        if(hd - sizes[i] < 0) break;
        
        hd -= sizes[i];
        storedFiles++;
      }
      
      return storedFiles;
    }
    
