//x is the current element, from str, select if x is not a number, concat the elements and return the result.
using System;
using System.Linq;

public class Kata{
  public static string FilterNumbers(string str) => string.Concat(str.Where(x => !Char.IsNumber(x)));
}

//****************Sample Test*****************
using System;
using NUnit.Framework;
using System.Collections.Generic;
using System.Linq;

[TestFixture]
public class Tests
{
  [Test]
  public static void FixedTest()
  {
    Assert.AreEqual("test", Kata.FilterNumbers("test123"));
    Assert.AreEqual("abc", Kata.FilterNumbers("a1b2c3"));
    Assert.AreEqual("aabbccdd", Kata.FilterNumbers("aa1bb2cc3dd"));
  }
  
  [Test]
  public static void RandomTests()
  {
    for(int i=0;i<47;i++){
      string g = System.Guid.NewGuid().ToString();
      string ans = String.Copy(g);
      Assert.AreEqual(solve(ans), Kata.FilterNumbers(g));
    }
  }
  
  private static string solve(string ans){
    return new string(ans.ToCharArray().Where(c => !Char.IsNumber(c)).ToArray());
  }
}
