//compare area of shapes, return 1, -1, 0 accordingly
//https://learn.microsoft.com/en-us/dotnet/api/system.icomparable-1?view=net-8.0#remarks
using System;
public class Shape : IComparable<Shape>{
    protected double Area = 0;
    public int CompareTo(Shape other) => Area > other.Area ? 
                                         1 : Area < other.Area ? 
                                        -1 : 0;
}

//find area of shapes
public class Square      : Shape{ public Square     (double s)                   {Area = s * s;}}
public class Rectangle   : Shape{ public Rectangle  (double w, double h)         {Area = w * h;}}
public class Circle      : Shape{ public Circle     (double r)                   {Area = r * r * Math.PI;}}
public class Triangle    : Shape{ public Triangle   (double triBase, double h)   {Area = triBase * h * 0.5;}}
public class CustomShape : Shape{ public CustomShape(double Area)                {this.Area = Area;}}



