using NUnit.Framework;
using System;
using System.Linq;
using System.Collections.Generic;

[TestFixture]
public class ShapesTests
{
    [Test]
    public void ShapesAreSortableOnArea()
    {
        // Arrange
        double width, height, triangleBase, side, radius, area;
        Random random = new Random((int)DateTime.UtcNow.Ticks);

        var expected = new List<Shape>();

        area = 1.1234;
        expected.Add(new CustomShape(area));

        side = 1.1234;
        expected.Add(new Square(side));

        radius = 1.1234;
        expected.Add(new Circle(radius));

        triangleBase = 5;
        height = 2;
        expected.Add(new Triangle(triangleBase, height));

        height = 3;
        triangleBase = 4;
        expected.Add(new Triangle(triangleBase, height));

        width = 4;
        expected.Add(new Rectangle(width, height));

        area = 16.1;
        expected.Add(new CustomShape(area));

        var actual = expected.OrderBy(x => random.Next()).ToList(); 
        
        // Act
        actual.Sort();

        // Assert
        for (var i = 0; i < 5; i++)
            Assert.AreEqual(expected[i], actual[i]);
    }

    [Test]
    public void RandomizedTest()
    {
        double side, width, height, triangleBase, area, radius;
        var random = new Random((int)DateTime.UtcNow.Ticks);
        Dictionary<double, Shape> shapes = new Dictionary<double, Shape>();
        List<Shape> actual = new List<Shape>();
        for (var i = 0; i < 25000; i++)
        {
            Shape shape;
            switch(random.Next(0, 5))
            {
                case 0:
                    side = random.NextDouble();
                    shape = new Square(side);
                    area = Math.Pow(side, 2);
                    break;
                case 1:
                    width = random.NextDouble();
                    height = random.NextDouble();
                    shape = new Rectangle(width, height);
                    area = width * height;
                    break;
                case 2:
                    triangleBase = random.NextDouble();
                    height = random.NextDouble();
                    shape = new Triangle(triangleBase, height);
                    area = (triangleBase * height) / 2;
                    break;
                case 3:
                    radius = random.NextDouble();
                    shape = new Circle(radius);
                    area = (Math.Pow(radius, 2) * Math.PI);
                    break;
                default:
                    area = random.NextDouble();
                    shape = new CustomShape(area);
                    break;
            }
            if (!shapes.ContainsKey(area))
            {
                shapes.Add(area, shape);
                actual.Add(shape);
            }
        }
        var expected = shapes.OrderBy(x => x.Key).Select(x => x.Value).ToList();
        actual.Sort();
        for (var j = 0; j < shapes.Count; j++)
            Assert.AreEqual(expected[j], actual[j]);
    }
