using System.Linq;

public class Kata{
  public static object[] CountSel(int[] lst){
    //repeat use
    var numGroup = lst.GroupBy(x => x).ToArray();
    
    //1 
    int len = lst.Length;
  
    //2
    int distinctNum = numGroup.Count();
    
    //3
    int occur = numGroup.Count(x => x.Count() == 1);
    
    //5
    int maxOccur = numGroup.Max(x => x.Count());
      
    //4
    var maxList = numGroup.Where(x => x.Count() == maxOccur)
                          .Select(x => x.Key)
                          .OrderBy(x => x)
                          .ToArray();
      
    return new object[] {len, distinctNum, occur, new object[]{ maxList , maxOccur}};
  }
}

