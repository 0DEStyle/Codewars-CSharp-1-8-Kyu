    [Test]
    public void RandomTests()
    {
      var rand = new Random();
      
      Func<int[], object[]> myCountSel = delegate (int[] lst)
      {
        var maxCount = lst.GroupBy(a => a).Max(a => a.Count());
        var maxNumbers = lst.GroupBy(a => a).Where(a => a.Count() == maxCount).Select(a => a.Key).OrderBy(a => a).ToArray();
        var differentNumbersCount = lst.GroupBy(a => a).Where(a => a.Count() == 1).Count();
    
        return new object[] { lst.Length, lst.Distinct().Count(), differentNumbersCount, new object[] { maxNumbers, maxCount }};
      };
      
      for (var h = 0; h <= 400; h++) 
      {
        var e = rand.Next(20,3000);
        var n = rand.Next(30,500);
        
        var lst_ = Enumerable.Range(0, n).Select(a => rand.Next(-e,e)).ToArray();
        
        Console.WriteLine("Testing for an array with length = " + lst_.Length);
        var expected = myCountSel(lst_);
        var result = Kata.CountSel(lst_);        
        
        Assert.AreEqual(AsString(expected), AsString(result));
      }
    }
    
    private string AsString(object[] o)
    {
      if(o == null)
      {
        return "[null]";
      }
      return string.Join(",", o.Select(a => a.GetType().IsArray ? "[[" + string.Join(",", (int[])((object[])a)[0]) + "], " + (int)((object[])a)[1] + "]" : a));
    }    
