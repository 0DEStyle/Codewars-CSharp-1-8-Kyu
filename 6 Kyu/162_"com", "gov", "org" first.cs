using System.Linq;
using System.Collections.Generic;

namespace CodeWarsTasks
{
  public static class SortingTask
  {
    public static IEnumerable<string> OrderByDomain(this IEnumerable<string> source)
    {
      return source.OrderBy(x => !(x.Contains(".com") || x.Contains(".gov") || x.Contains(".org"))).ThenBy(x => x[x.LastIndexOf(".")..]);
    }
  }
}
