    public static long[][] PascalSol(long depth)
    {
  		long[][] triangle = new long[depth][];
  		for(var i=0; i<depth; i++) {
  			long[] tier = new long[i+1];
  			for(var j=0; j<=i; j++)
  				if(j == 0 || j == i) tier[j] = 1;
  				else tier[j] = triangle[i-1][j-1] + triangle[i-1][j];
  			triangle[i] = tier;
  		}
  		return triangle;
    }
    
    static Random rng = new Random();
    public static int RandInt(int min, int max) {
      return rng.Next(min, max);
    }
    
    [Test]
    public void RandomTests()
    {
      int randNum;
      long[][] expected;
      
      for (var i = 1; i < 30; i++) {
        randNum = RandInt(1, 68);
        expected = PascalSol(randNum);
        Assert.AreEqual(expected, Kata.Pascal(randNum), "Failed a random test");
      }
    }
