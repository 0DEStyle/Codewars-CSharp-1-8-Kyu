using System;

public class Kata{
  public static long[][] Pascal(long n){ 
    var numArr = new long [n][];
    
    for(var i = 0; i < n; i++){
      //create new row, set index of 0 and i to 1
      numArr[i] = new long [i + 1];
      numArr[i][0] = 1;
      numArr[i][i] = 1;
      
      //set current row to the sum of two numbers above it from the previous row
      for(var j = 1; j < i; j++)
        numArr[i][j] = numArr[i - 1][j] + numArr[i - 1][j - 1];
    }
    //check result
    //printJank(numArr);
    return numArr;
      
  }
  
  private static void printJank(long[][] arr){
    for(var i = 0; i < arr.Length; i++)
        Console.WriteLine(string.Join(",",arr[i]));
    } 
}

