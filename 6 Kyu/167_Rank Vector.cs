using System.Linq;

public class Kata {
  public static int[] Ranks(int[] ranks) {
    var ordered = ranks.OrderByDescending(r => r).ToList();
    return ranks.Select(r => ordered.IndexOf(r) + 1).ToArray();
  }
}
